
//This is a demonstration of comments within a Java source file.
//Utilizing the double forward slash (//) treats everything after it as a comment
public class Comments { //comments can also appear on the same line as code elements

    /*
    Comments can also be formatted into multi-line variety which include everything
    starting with (/*) and continue until the reverse ordering of those characters is
    found.
     */
    // Comments can appear anywhere within the file. They can be next to classes, methods, variables
    // or even at the end of the file.
    public static void main(String[] args) {
        System.out.println("This is the start of the Comments program"); //this informs the user the program is starting
        //Now the program is ending
        System.out.println("This is the end of the Comments program");
    }
}