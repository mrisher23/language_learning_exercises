//All Java code must be contained within a Class block.
//The name of the class should match the name of the file.
public class HelloWorld {
    //Any Java file that can be run, must have a function matching this
    //exact signature.
    public static void main(String[] args) {
        //The System library (provided by core Java) contains an data stream to standard out
        //called 'out'. The data stream has a function called 'print' which takes a string argument.
        System.out.print("Hello World");
    }
}
